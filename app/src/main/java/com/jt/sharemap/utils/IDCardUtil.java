    /**
     * 判断身份证：要么是15位，要么是18位，最后一位可以为字母
     *
     * @param IDStr
     * @return
     */
    public static boolean SampleIDCardValidate(String IDStr) {
        boolean isID = false;
        //通过Pattern获得Matcher
        Matcher idNumMatcher = idNumPattern.matcher(IDStr);
        //判断用户输入是否为身份证号
        if (idNumMatcher.matches()) {
            isID = true;
        } else {
            isID = false;
        }
        return isID;
    }

